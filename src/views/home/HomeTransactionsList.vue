<template>
  <div class="transactions apex-modul fr">
    <p class="apex-title">
      Transactions
      <router-link to="/transactions">
        <span>ALL</span>
      </router-link>
    </p>
    <ul class="apex-list">
      <vue-scroll :ops="ops">
        <li v-for="(item,index) in transactions" :key="index">
          <div class="bottom">
            <router-link
              to="/transactions/TransactionsInfo"
              @click.native="setClickValue"
            >{{item.txHash}}</router-link>
            <span>{{item.refBlockTime}}</span>
          </div>
        </li>
      </vue-scroll>
    </ul>
  </div>
</template>

<script>
import Pagination from "@/components/public/Pagination.vue";
import Bus from "./../../utils/bus";
import util from "./../../utils/utils";
export default {
  name: "HomeTransactionsList",
  components: {
    Pagination
  },
  data() {
    return {
      title: "Transactions",
      transactions: [],
      ops: {},
      clickValue: null,
      transaction_list_url: "/api/v1.0/transactions/transactionList",
      parmas: {
        start: "0",
        pageSize: "20"
      }
    };
  },
  created() {},
  mounted() {
    this.getAllTransactions();
    const timer = setInterval(() => {
      this.getAllTransactions();
    }, 1500);
    this.$once("hook:beforeDestroy", () => {
      clearInterval(timer);
    });
  },
  methods: {
    getAllTransactions() {
      this.$axios
        .post(this.transaction_list_url, this.parmas)
        .then(response => {
          let res = response.data.data;
          let serverTime = response.headers.date;
          let time;
          for (let i = 0; i < res.length; i++) {
            const item = res[i];
            time = util.utilMethods.listUTCtime(item.refBlockTime, serverTime);
            item.refBlockTime = time;
          }
          this.transactions = res;
        })
        .catch(function(err) {
          if (err.response) {
          }
        });
    },
    setClickValue(e) {
      Bus.$emit("txHash", e.target.innerHTML);
    },
    offListener() {
      Bus.$off("txHash");
    }
  },
  beforeDestroy() {
    this.offListener();
  }
};
</script>

