<template>
  <div class="home clearboth">
    <div class="top-modul clearboth">
      <wallet-page :address="apAddress" :privKey="privKey"/>
    </div>
    <div class="bottom-modul clearboth">
      <transfers-list/>
      <transactions-list/>
    </div>
  </div>
</template>
<script>
import WalletPage from "@/views/wallet/wallet/walletPage";
import TransfersList from "@/components/list/TransfersList";
import TransactionsList from "@/components/list/TransactionsList";
import Bus from "./../../utils/bus";

export default {
  name: "wallet",
  components: {
    WalletPage,
    TransfersList,
    TransactionsList
  },
  data() {
    return {
      apAddress: null,
      privKey: null
    };
  },
  mounted() {
    this.getlastAddress();
  },
  methods: {
    getlastAddress() {
      Bus.$on("apAddress", data => {
        this.apAddress = data;
      });
      Bus.$on("privKey", data => {
        this.privKey = data;
      });
    }
  },
  computed: {}
};
</script>
